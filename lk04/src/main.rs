fn main() {
    // 元祖的学习,用括号包扩起来，类型多种多样
    let mup:(i32,f64,u8) = (500,6.5,12);
    let (a,b,c) = mup;
    println!("a={:?},b={:?},c={:?}",a,b,c);
    // 定义变量c，类型i32，数组为5个元素的数字，数组必须用大括号，不然报错
    let c: [i32; 5] = [1, 2, 3, 4, 5];
    let x =c;
    println!("x={:?}",x);
    // 定义一个数组，就是5个3，原型let d :[i32,5]=[3,3,3,3,3,3]
    let d =[3;5];
    let e = d;
    print!("e={:?}",e)

}
